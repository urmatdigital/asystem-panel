/**
 * intent-recognizer.mjs — Intention Recognition & Ambiguity Clarification
 *
 * Video: "Zendesk AI Agents: Setup, Costs, and Best Practices (2026)" (zaWri275JUk)
 * Pattern: Detect ambiguous tasks BEFORE dispatch → request clarification
 *
 * Intent classification:
 *   1. Parse task title → extract primary verb + object
 *   2. Match against intent taxonomy (8 categories)
 *   3. Score ambiguity (0-1) based on:
 *      - Missing scope (which project? which module?)
 *      - Vague verbs (improve, update, handle, manage, deal with)
 *      - Conflicting signals (fix + add = unclear priority)
 *      - No measurable outcome (no success criteria)
 *   4. If ambiguity > 0.6 → generate clarifying questions (max 3)
 *   5. Optionally block dispatch until clarification received
 *
 * Intent categories:
 *   CREATE, FIX, REVIEW, DEPLOY, DOCUMENT, RESEARCH, TEST, CONFIGURE
 *
 * Clarification questions generated by pattern:
 *   scope     → "Which project/module does this apply to?"
 *   outcome   → "What does success look like for this task?"
 *   priority  → "Is this blocking anything? What's the deadline?"
 *   ownership → "Who should implement this?"
 *
 * API:
 *   POST /api/intent/analyze   { title, desc?, to?, priority? } → { intent, ambiguity, questions, recommendation }
 *   GET  /api/intent/taxonomy  → intent taxonomy with examples
 */

const INTENT_MAP = {
  CREATE:    { verbs: ['create', 'build', 'implement', 'write', 'develop', 'add', 'generate', 'scaffold', 'init', 'setup', 'make'], emoji: '🏗️' },
  FIX:       { verbs: ['fix', 'debug', 'repair', 'resolve', 'patch', 'correct', 'solve', 'address', 'handle bug', 'investigate'], emoji: '🔧' },
  REVIEW:    { verbs: ['review', 'audit', 'check', 'verify', 'validate', 'inspect', 'assess', 'evaluate', 'analyze', 'test'], emoji: '🔍' },
  DEPLOY:    { verbs: ['deploy', 'release', 'publish', 'ship', 'rollout', 'push to', 'go live', 'launch', 'migrate'], emoji: '🚀' },
  DOCUMENT:  { verbs: ['document', 'write docs', 'readme', 'update docs', 'api docs', 'comment', 'explain'], emoji: '📝' },
  RESEARCH:  { verbs: ['research', 'investigate', 'explore', 'analyze', 'study', 'compare', 'benchmark', 'evaluate options'], emoji: '🔬' },
  TEST:      { verbs: ['test', 'write tests', 'unit test', 'integration test', 'e2e', 'qa', 'regression', 'coverage'], emoji: '🧪' },
  CONFIGURE: { verbs: ['configure', 'setup', 'install', 'enable', 'disable', 'tune', 'adjust', 'set up', 'integrate'], emoji: '⚙️' },
};

// Vague verbs that indicate ambiguity
const VAGUE_VERBS = ['improve', 'update', 'handle', 'manage', 'deal', 'work on', 'look at', 'check out', 'do something', 'take care', 'enhance', 'optimize', 'refactor', 'clean up'];

// Scope keywords that resolve ambiguity
const SCOPE_KEYWORDS = ['in', 'for', 'on', 'at', 'inside', 'within', 'orgon', 'aurwa', 'fiatex', 'voltera', 'asystem', 'module', 'component', 'endpoint', 'route', 'page', 'api'];

// Outcome keywords
const OUTCOME_KEYWORDS = ['so that', 'to allow', 'which will', 'enabling', 'goal:', 'success:', 'acceptance criteria', 'until', 'when done', 'result should'];

export function analyzeIntent(title = '', desc = '', to = '', priority = 'medium') {
  const text = (title + ' ' + (desc || '')).toLowerCase().trim();
  const words = text.split(/\s+/);

  // Classify intent
  let detectedIntent = 'UNKNOWN';
  let intentScore = 0;
  for (const [intent, cfg] of Object.entries(INTENT_MAP)) {
    const matches = cfg.verbs.filter(v => text.includes(v)).length;
    if (matches > intentScore) { intentScore = matches; detectedIntent = intent; }
  }

  // Ambiguity signals
  const signals = [];

  // 1. Vague verb
  const hasVague = VAGUE_VERBS.some(v => text.startsWith(v) || text.includes(' ' + v + ' '));
  if (hasVague) signals.push({ type: 'vague_verb', weight: 0.35, q: 'What specific change or outcome is expected?' });

  // 2. Missing scope
  const hasScope = SCOPE_KEYWORDS.some(k => text.includes(k));
  if (!hasScope && title.split(' ').length < 6) signals.push({ type: 'missing_scope', weight: 0.25, q: 'Which project, module, or component does this apply to?' });

  // 3. No measurable outcome
  const hasOutcome = OUTCOME_KEYWORDS.some(k => text.includes(k));
  if (!hasOutcome && detectedIntent !== 'FIX') signals.push({ type: 'no_outcome', weight: 0.2, q: 'What does success look like? How will we verify completion?' });

  // 4. No agent specified
  if (!to) signals.push({ type: 'no_owner', weight: 0.1, q: 'Who should implement this? (bekzat=backend, ainura=frontend, marat=qa)' });

  // 5. Deadline for critical
  if (priority === 'critical' && !text.includes('by ') && !text.includes('before ') && !text.includes('deadline')) {
    signals.push({ type: 'no_deadline', weight: 0.1, q: 'This is critical — what is the deadline or blocking event?' });
  }

  // Ambiguity score
  const ambiguity = Math.min(1, signals.reduce((s, sg) => s + sg.weight, 0));
  const questions = signals.slice(0, 3).map(s => s.q);

  const recommendation = ambiguity >= 0.6
    ? 'CLARIFY_BEFORE_DISPATCH'
    : ambiguity >= 0.3
    ? 'PROCEED_WITH_CAUTION'
    : 'PROCEED';

  const intentCfg = INTENT_MAP[detectedIntent];

  return {
    intent: detectedIntent,
    emoji: intentCfg?.emoji || '❓',
    ambiguity: Math.round(ambiguity * 100) / 100,
    signals: signals.map(s => s.type),
    questions,
    recommendation,
    advice: recommendation === 'CLARIFY_BEFORE_DISPATCH'
      ? `High ambiguity (${Math.round(ambiguity * 100)}%) — ask these ${questions.length} questions before dispatching`
      : recommendation === 'PROCEED_WITH_CAUTION'
      ? `Some ambiguity (${Math.round(ambiguity * 100)}%) — add clarification to task description`
      : `Clear task (${Math.round(ambiguity * 100)}% ambiguity) — safe to dispatch`,
  };
}

export function getIntentTaxonomy() {
  return Object.fromEntries(Object.entries(INTENT_MAP).map(([k, v]) => [k, { emoji: v.emoji, verbs: v.verbs.slice(0, 5) }]));
}
